    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        Musica musicaSelecionada = (Musica) cbxMusica.getSelectedItem();
        if (musicaSelecionada == null) {
            JOptionPane.showMessageDialog(this, "Selecione uma música!");
            return;
        }

        PlaylistController controller = new PlaylistController();
        boolean ok = controller.adicionarMusicaNaPlaylist(idPlaylist, musicaSelecionada.getPkmusica());

        if (ok) {
            JOptionPane.showMessageDialog(this, "Música adicionada à playlist com sucesso!");
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao adicionar música à playlist.");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

