    private void carregarMusicas() {
        MusicaController musicaController = new MusicaController();
        List<Musica> musicas = musicaController.buscarTodas(); // certifique-se que esse método existe

        DefaultComboBoxModel<Musica> model = new DefaultComboBoxModel<>();
        for (Musica m : musicas) {
            model.addElement(m);
        }
        cbxMusica.setModel(model);
    }

