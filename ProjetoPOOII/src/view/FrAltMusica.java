    private void carregarArtistas() {
        ArtistaController controller = new ArtistaController();
        List<Artista> lista = controller.listar();

        cbxArtista.removeAllItems();
        for (Artista art : lista) {
            cbxArtista.addItem(art);
        }
    }

// Método para carregar os dados da música no formulário
    private void carregarDadosMusica() {
        MusicaController controller = new MusicaController();
        List<Musica> lista = controller.consultar(0, ""); // ou consultar pelo PK
        for (Musica m : lista) {
            if (m.getPkmusica() == pkMusica) {

                // Seleciona o artista no combo
                for (int i = 0; i < cbxArtista.getItemCount(); i++) {
                    if (cbxArtista.getItemAt(i).getPkartista() == m.getArtista().getPkartista()) {
                        cbxArtista.setSelectedIndex(i);
                        break;
                    }
                }
                break;
            }
        }
    }

