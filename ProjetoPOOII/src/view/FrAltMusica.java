    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        Artista artistaSelecionado = (Artista) cbxArtista.getSelectedItem();
        if (artistaSelecionado == null) {
            JOptionPane.showMessageDialog(this, "Selecione um artista!");
            return;
        }

        MusicaController controller = new MusicaController();
        boolean ok = controller.adicionarColaboracao(pkMusica, artistaSelecionado.getPkartista());

        if (ok) {
            JOptionPane.showMessageDialog(this, "Artista adicionado à música com sucesso!");
            this.dispose();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

