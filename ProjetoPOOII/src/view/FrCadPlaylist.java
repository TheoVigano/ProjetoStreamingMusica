    private void gravarPlaylist() {
        String nome = edtNome.getText().trim();

        if (nome.isEmpty()) {
            JOptionPane.showMessageDialog(this, "O nome da playlist nÃ£o pode estar vazio!");
            return;
        }

        GerenciadorConexao gerenciador = new GerenciadorConexao();
        PreparedStatement comando = null;

        try {
            String sql = "INSERT INTO tbplaylist (nome, fkusuario) VALUES (?, ?)";
            comando = gerenciador.prepararComando(sql);
            comando.setString(1, nome);
            comando.setInt(2, usuarioLogadoId); // garante que o fkusuario exista

            int linhas = comando.executeUpdate();

            if (linhas > 0) {
                JOptionPane.showMessageDialog(this, "Playlist cadastrada com sucesso!");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao cadastrar playlist!");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
        } finally {
            gerenciador.fecharConexao(comando);
        }
    }

